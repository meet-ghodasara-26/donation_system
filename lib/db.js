/**
 * lib/db.js
 *
 * File-based SQL storage.
 * All donations are persisted as INSERT statements inside data/donations.sql
 *
 * Format of donations.sql:
 *   INSERT INTO donations (id, name, nameGu, city, cityGu, message, messageGu, amount, date, profileImage)
 *   VALUES (1, 'Ram', 'રામ', 'Surat', 'સુરત', '', '', 5000, '2025-01-01T00:00:00.000Z', NULL);
 *
 * On every write the file is fully regenerated so it stays clean and consistent.
 */

import fs   from 'fs';
import path from 'path';

const SQL_FILE = path.join(process.cwd(), 'data', 'donations.sql');

// ── Helpers ────────────────────────────────────────────────────────────────

/** Escape a string value for safe embedding inside single-quoted SQL literal */
function esc(val) {
  if (val === null || val === undefined) return 'NULL';
  return `'${String(val).replace(/'/g, "''")}'`;
}

/** Parse a single-quoted SQL string literal back to a JS string */
function unesc(token) {
  if (token === 'NULL') return null;
  // strip surrounding quotes and unescape doubled single-quotes
  return token.slice(1, -1).replace(/''/g, "'");
}

/**
 * Parse the SQL file into an array of donation objects.
 * Each INSERT line is parsed by splitting on the VALUES clause.
 */
function readSqlFile() {
  if (!fs.existsSync(SQL_FILE)) return [];

  const lines = fs.readFileSync(SQL_FILE, 'utf-8').split('\n');
  const donations = [];

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('--')) continue;

    // Match: INSERT INTO donations (...) VALUES (...);
    const match = trimmed.match(/^INSERT INTO donations \([^)]+\)\s+VALUES\s+\((.+)\);$/i);
    if (!match) continue;

    try {
      const tokens = tokenizeValues(match[1]);
      if (tokens.length < 10) continue;

      donations.push({
        id:           Number(tokens[0]),
        name:         unesc(tokens[1]),
        nameGu:       unesc(tokens[2]),
        city:         unesc(tokens[3]),
        cityGu:       unesc(tokens[4]),
        message:      unesc(tokens[5]) || '',
        messageGu:    unesc(tokens[6]) || '',
        amount:       Number(tokens[7]),
        date:         unesc(tokens[8]),
        profileImage: unesc(tokens[9]),
      });
    } catch {
      // skip malformed lines
    }
  }

  return donations;
}

/**
 * Tokenize the VALUES(...) content respecting single-quoted strings with
 * embedded escaped quotes ('').
 */
function tokenizeValues(str) {
  const tokens = [];
  let i = 0;

  while (i < str.length) {
    // skip whitespace and commas between tokens
    while (i < str.length && (str[i] === ',' || str[i] === ' ')) i++;
    if (i >= str.length) break;

    if (str[i] === "'") {
      // quoted string — consume until closing unescaped quote
      let j = i + 1;
      let value = "'";
      while (j < str.length) {
        if (str[j] === "'" && str[j + 1] === "'") {
          value += "''";
          j += 2;
        } else if (str[j] === "'") {
          value += "'";
          j++;
          break;
        } else {
          value += str[j++];
        }
      }
      tokens.push(value);
      i = j;
    } else {
      // unquoted token (number or NULL)
      let j = i;
      while (j < str.length && str[j] !== ',' && str[j] !== ')') j++;
      tokens.push(str.slice(i, j).trim());
      i = j;
    }
  }

  return tokens;
}

/**
 * Serialise one donation object to an INSERT line.
 */
function toInsertLine(d) {
  return (
    `INSERT INTO donations (id, name, nameGu, city, cityGu, message, messageGu, amount, date, profileImage) ` +
    `VALUES (${d.id}, ${esc(d.name)}, ${esc(d.nameGu)}, ${esc(d.city)}, ${esc(d.cityGu)}, ` +
    `${esc(d.message)}, ${esc(d.messageGu)}, ${d.amount}, ${esc(d.date)}, ${esc(d.profileImage)});`
  );
}

/**
 * Write the full list back to donations.sql, sorted by amount descending.
 */
function writeSqlFile(donations) {
  // ensure data/ directory exists
  const dir = path.dirname(SQL_FILE);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

  const sorted = [...donations].sort((a, b) => b.amount - a.amount);

  const header = [
    '-- donations.sql',
    '-- Auto-generated by Donation Management System',
    `-- Last updated: ${new Date().toISOString()}`,
    '--',
    '-- Each row is one donation INSERT statement, sorted by amount DESC.',
    '',
  ].join('\n');

  const body = sorted.map(toInsertLine).join('\n');
  fs.writeFileSync(SQL_FILE, header + body + '\n', 'utf-8');
}

// ── Public API (same interface the route.js already uses) ──────────────────

/**
 * Return all donations sorted by amount descending.
 * @returns {Array}
 */
export function getAllDonations() {
  return readSqlFile().sort((a, b) => b.amount - a.amount);
}

/**
 * Append a new donation, persist to donations.sql, return the saved object.
 * @param {Object} data
 * @returns {Object}
 */
export function createDonation(data) {
  const donations = readSqlFile();

  const maxId = donations.length > 0 ? Math.max(...donations.map((d) => d.id)) : 0;

  const newDonation = {
    id:           maxId + 1,
    name:         data.name,
    nameGu:       data.nameGu   || data.name,
    city:         data.city,
    cityGu:       data.cityGu   || data.city,
    message:      data.message  || '',
    messageGu:    data.messageGu|| '',
    amount:       Number(data.amount),
    date:         data.date || new Date().toISOString(),
    profileImage: data.profileImage || null,
  };

  donations.push(newDonation);
  writeSqlFile(donations);

  return newDonation;
}
